package com.infinity.infoway.university_demo.login.pojo;

public class RegisterStudentDetailsModel {

    String stuPassword;
    String stuEnrollmentNo;
    String studentName;

    public String getStuPassword() {
        return stuPassword;
    }

    public void setStuPassword(String stuPassword) {
        this.stuPassword = stuPassword;
    }

    public String getStuEnrollmentNo() {
        return stuEnrollmentNo;
    }

    public void setStuEnrollmentNo(String stuEnrollmentNo) {
        this.stuEnrollmentNo = stuEnrollmentNo;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

}
